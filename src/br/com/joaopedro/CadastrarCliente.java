        // TODO add your handling code here:
        try{
            if(idAlteracao == 0){
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pizzaria","Gerente","1234");
                String sql = "insert,delete,update,select into cliente(ID,Nome,Telefone,DDD,Email,CPF,RG,"
                        + "DtNascimento) values (?,?,?,?,?,?,?)";
                PreparedStatement stmt = con.prepareStatement(sql);
                stmt.setString(1, this.txtNome.getText());
                stmt.setInt(2, Integer.parseInt(this.txtTelefone.getText()));
                stmt.setInt(3, Integer.parseInt(this.txtDDD.getText()));
                stmt.setString(4, this.txtEmail.getText());
                stmt.setString(5, this.txtCPF.getText());
                stmt.setString(6, this.txtRG.getText());
                SimpleDateFormat df = new SimpleDateFormat("yyyy/MM/dd"); 
                Date dt = new Date(df.parse(txtDtnascimento.getText()).getTime());
                stmt.setDate(7, dt);
                stmt.execute();
                stmt.close();
                con.close();
            }else{
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pizzaria","atendente","1515");
                String sql = "update,insert,select cliente set Nome=?,Telefone=?,DDD=?,Email=?"
                        + ",CPF=?,RG=?,DtNascimento=? where idCliente = ?";
                PreparedStatement stmt = con.prepareStatement(sql);
               stmt.setString(1, this.txtID.getText());
                stmt.setString(2, this.txtNome.getText());
                stmt.setInt(3, Integer.parseInt(this.txtTelefone.getText()));
                stmt.setInt(4, Integer.parseInt(this.txtDDD.getText()));
                stmt.setString(5, this.txtEmail.getText());
                stmt.setString(6, this.txtCPF.getText());
                stmt.setString(7, this.txtRG.getText());
                SimpleDateFormat df = new SimpleDateFormat("yyyy/MM/dd"); 
                Date dt = new Date(df.parse(txtDtnascimento.getText()).getTime());
                stmt.setDate(8, dt);
                stmt.setInt(9, idAlteracao);
                stmt.execute();
                stmt.close();
                con.close();
            }
            JOptionPane.showMessageDialog(this, "Cliente Cadastrado com Sucesso!");
            this.dispose();
        }catch(SQLException | ParseException e){
            JOptionPane.showMessageDialog(null,"Acesso Bloqueado, nivel de usuario Atendente");
        }
        
